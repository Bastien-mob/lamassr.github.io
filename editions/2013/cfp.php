				<h3>Contribution Submission</h3>

				<p>
					Submitted contributions should not exceed five pages using the <a href = "http://style.eptcs.org/">EPTCS
					format</a>.
					If necessary, the work may be supplemented with a clearly marked appendix, which will be consulted at the
					discretion of the program committee.
					Submissions must be in PDF format and will be handled via
					<a href = "https://www.easychair.org/conferences/?conf=sr2013">EasyChair</a>.
				</p>

				<p>
					Two types of submission are invited:
				</p>

				<ul>
					<li>contributions reporting on novel research;</li>
					<li>expository contributions reporting on published work.</li>
				</ul>

				<p>
					Each submission should be clearly identified as belonging to one category or the other.
					In both categories, strong preference will be given to contributions whose topic is of interest to an
					interdisciplinary audience and all contributions should be written so that they are accessible to such an
					audience.
				</p>

				<p>
					Novel research abstracts will be held to the usual high standards of novel research publications.
					In particular, they should
				</p>

				<ol>
					<li>
						contain enough information to enable the program committee to identify the main contribution of the work,
					</li>
					<li>
						explain the significance of the work, its novelty, and its practical or theoretical implications, and
					</li>
					<li>
						include comparisons with and references to relevant literature.
					</li>
				</ol>

				<p>
					Expository abstracts, which will be held to similarly high standards, may survey an area or report on a more
					specific previously published work.
				</p>

				<p>
					Submissions should make clear the relevance to the strategic reasoning audience.
				</p>

				<p>
					Contributions accepted for presentation (in both categories) will be collected as an EPTCS volume.
					Submissions from PC members are also allowed.
					Note that, as we accept short contributions, extended versions of the accepted works could be also submitted
					elsewhere.
				</p>

