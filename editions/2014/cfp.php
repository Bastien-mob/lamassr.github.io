				<h3>Objectives</h3>

				<p>
					Strategic reasoning is one of the most active research area in multi-agent system domain.
					The literature in this field is extensive and provides a plethora of logics for modeling strategic ability.
					Theoretical results are now being used in many exciting domains, including software tools for information
					system security, robot teams with sophisticated adaptive strategies, and automatic players capable of beating
					expert human adversary, just to cite a few.
					All these examples share the challenge of developing novel theories and tools for agent strategies that take
					into account the likely behavior of adversaries.
				</p>

				<p>
					The SR international workshop aims to bring together researchers working on different aspects of strategic
					reasoning in computer science, both from a theoretical and a practical point of view.
				</p>

				<h3>List of Topics</h3>

				<p>
					The topics covered by the workshop include, but are not limited to, the following:
				</p>

				<ul>
					<li>Logics for reasoning about strategic abilities;</li>
					<li>Logics for multi-agent mechanism design, verification, and synthesis;</li>
					<li>Logical foundations of decision theory for multi-agent systems;</li>
					<li>Strategic reasoning in formal verification;</li>
					<li>Automata theory for strategy synthesis;</li>
					<li>Strategic reasoning under perfect and imperfect information;</li>
					<li>Applications and tools for cooperative and adversarial reasoning;</li>
					<li>Robust planning and optimization in multi-agent systems;</li>
					<li>Risk and uncertainty in multi-agent systems;</li>
					<li>Quantitative aspects in strategic reasonings.</li>
				</ul>

