<h1>Call for contributions</h1>

<p>
Strategic reasoning is a key topic in multi-agent systems research.
The extensive literature in the field includes a variety of logics used
for modeling strategic ability. Results from the field
are now being used in many exciting domains such as
information system security, adaptive strategies for robot teams,
and automatic players capable to outperform human experts.
A common feature in all these application domains is the requirement for
sound theoretical foundations and tools accounting for the strategies
that artificial agents may adopt in the situation of conflict and 
cooperation.
</p>

<p>
The SR international workshop series aims at bringing together researchers 
working on different aspects of strategic reasoning in computer science, both from a
theoretical and a practical point of view.
</p>

<p>
SR 2020 will be held with ECAI 2020 in Santiago de Compostela, Spain.
</p>

<h2>Topics of interest</h2>
<p>The topics covered by SR include, but are not limited to, the
following:
<ul>
<li> Logics for reasoning about strategic abilities;</li>
<li> Logics for multi-agent mechanism design, verification, and synthesis;</li>
<li> Logical foundations of decision theory for multi-agent systems;</li>
<li> Strategic reasoning in formal verification;</li>
<li> Automata theory for strategy synthesis;</li>
<li> Strategic reasoning under perfect and imperfect information;</li>
<li> Applications and tools for cooperative and adversarial reasoning;</li>
<li> Robust planning and optimisation in multi-agent systems;</li>
<li> Risk and uncertainty in multi-agent systems;</li>
<li> Quantitative aspects in strategic reasonings.</li>
</ul></p>

<h2>Submissions</h2>
<p>We invite three types of submissions:
<ul>
<li>(A) original contributions,</li>
<li>(B) published work, and</li>
<li>(C) challenging open problems.</li>
</ul>
Each submission should be clearly identified as belonging to one of
these three categories.
</p>

<p>In all three categories, submissions will be evaluated by the usual
high standards of research publications. In particular, they should
contain enough detail to allow the program committee to identify
the main contribution of the work, to explain its significance, its
novelty, its relevance to the strategic-reasoning audience, and its
practical or theoretical implications, and include comparisons with
and references to relevant literature.
</p>

<p>Strong preference will be given to contributions on topics of interest
to a broad, interdisciplinary audience and all papers should be
written so that they are accessible to such an audience.
</p>

<p>
Submissions should be in PDF, preferably using the EPTCS style
(<a href=http://style.eptcs.org/>http://style.eptcs.org</a>), and not exceed 12 pages (not including 
bibliography).
</p>

<p>
Authors are invited to submit their manuscript via EasyChair:
<a href='https://easychair.org/conferences/?conf=sr20200'>EasyChair</a>.</p>

<h2>Proceedings</h2>

<p>
All contributions will be made electronically available to the participants. There will be no formal proceedings.
</p>

<p>As for previous editions, depending on the quality of submissions, extended revised versions of selected original
papers may be invited to a special issue of Information & Computation.</p>


